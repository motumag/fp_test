const defaultTheme = createTheme();

export default function SignIn() {
  const [formData, setFormData] = useState({});
  const [registeLoading, setRegisterLoading] = useState(false);
  const [error, setError] = useState(false);
  const [actualMessage, setActualMessage] = useState("");
  const naviage = useNavigate();

  const handleChange = (e) => {
    // e.preventDefault();
    setFormData({ ...formData, [e.target.id]: e.target.value });
  };
  console.log("the form data is", formData);

  // const handleSubmit = (event) => {
  //   event.preventDefault();
  //   const data = new FormData(event.currentTarget);
  //   console.log({
  //     email: data.get("email"),
  //     password: data.get("password"),
  //   });
  // };

  const handleSubmit = async (event) => {
    event.preventDefault();
    try {
      setRegisterLoading(true);
      const resp = await fetch("/api/auth/sign-in", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify(formData),
      });
      const data = await resp.json();
      setActualMessage(data.message);
      setRegisterLoading(false);
      setError(false);
      if (data.success === false) {
        setError(true);
        return;
      }
      naviage("/dashboard");
      console.log("The actual message from backend is: ", actualMessage);
      console.log("the response is: ", data);
    } catch (error) {
      console.log(error);
      setError(true);
      setRegisterLoading(false);
    }
  };
  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: "secondary.main" }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box
            component="form"
            onSubmit={handleSubmit}
            noValidate
            sx={{ mt: 1 }}
          >
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
              onChange={handleChange}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
              onChange={handleChange}
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              {registeLoading ? "Signing in..." : "Sign In"}
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="/forgot-password" variant="body2">
                  Forgot password?
                </Link>
              </Grid>
              <Grid item>
                <Link href="/sign-up" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
          <p className="text-red-500">{error ? actualMessage : ""}</p>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}
